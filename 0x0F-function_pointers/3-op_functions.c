#include "3-calc.h"

/**
 * op_add - add two integers
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: integer
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function to sub two integers
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: integer
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function to multiply two integers
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: integer
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function to divide two integers
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: integer
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function to calculate mod of two integers
 *
 * @a: first integer
 *
 * @b: second integer
 *
 * Return: integer
*/

int op_mod(int a, int b)
{
	return (a % b);
}
